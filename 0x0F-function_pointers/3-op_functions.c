#include "3-calc.h"

/**
 * op_add - sum of a & b
 *
 * @a: first interger
 * @b: second integer
 *
 * Return: the summation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub of a & b
 *
 * @a: first interger
 * @b: second integer
 *
 * Return: the subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul of a & b
 *
 * @a: first interger
 * @b: second integer
 *
 * Return: the multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div of a & b
 *
 * @a: first interger
 * @b: second integer
 *
 * Return: the divison 
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod of a & b
 *
 * @a: first interger
 * @b: second integer
 *
 * Return: the module
 */

int op_mod(int a, int b)
{
	return (a % b);
}
